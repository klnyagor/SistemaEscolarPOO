    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        DefaultTableModel tabela = (DefaultTableModel) TabelaAlunos.getModel();
        tabela.setNumRows(0);
        AlunoDAO alunos = new AlunoDAO();
        for (Aluno a: alunos.listar()){
            tabela.addRow(new Object[]{a.getId(), a.getNome(), a.getEndereco(), a.getTelefone(), a.getEmail()});
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int linha = -1;
        linha = TabelaAlunos.getSelectedRow();
        if(linha >= 0){
            int id = (int) TabelaAlunos.getValueAt(linha, 0);
            String nome = (String) TabelaAlunos.getValueAt(linha, 1);
            String endereco = (String) TabelaAlunos.getValueAt(linha, 2);
            String telefone = (String) TabelaAlunos.getValueAt(linha, 3);
            String email = (String) TabelaAlunos.getValueAt(linha, 4);
            
            AlunoCadastro cadastro = new AlunoCadastro(menu, new Aluno(nome,endereco,telefone,email,id));
            controller.navegar(cadastro);
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha!");
        }
        
    }//GEN-LAST:event_btnEditarActionPerformed


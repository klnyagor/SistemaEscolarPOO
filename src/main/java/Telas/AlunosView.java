    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linha = -1;
        linha = TabelaAlunos.getSelectedRow();
        if(linha >= 0){
            int id = (int) TabelaAlunos.getValueAt(linha, 0);
            AlunoDAO alunos = new AlunoDAO();
            if(alunos.deletar(id)){
                JOptionPane.showMessageDialog(null, "Aluno Removido!");
                btnPesquisarActionPerformed(null);
            } else {
                JOptionPane.showMessageDialog(null, "Aluno não pode ser Removido!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed


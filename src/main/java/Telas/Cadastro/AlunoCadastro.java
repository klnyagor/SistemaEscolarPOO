    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed
        String msg = "Ops, algo de errado n√£o est√° certo..";
        try{
            int id = Integer.parseInt(alunoIdInput.getText());
            String nome = alunoNomeInput.getText();
            String endereco = alunoEnderecoInput.getText();
            String telefone = alunoEnderecoInput.getText();
            String email = alunoEmailInput.getText();
            aluno = new Aluno(nome, endereco, telefone, email, id);
            AlunoDAO alunos = new AlunoDAO();
        
            if(editar && alunos.atualizar(aluno)){
                msg = "Cadastro Atualizado!";
            
            } else if(alunos.adicionar(aluno)){
                msg = "Aluno Cadastrado!";
                //limpar formulario
                alunoIdInput.setText("");
                alunoNomeInput.setText("");
                alunoEnderecoInput.setText("");
                alunoTelefoneInput.setText("");
                alunoEmailInput.setText("");
            }
        } catch(Exception e){
            e.printStackTrace();
        } finally{
            JOptionPane.showMessageDialog(null,msg);
        }
        
        
    }//GEN-LAST:event_cadastrarActionPerformed


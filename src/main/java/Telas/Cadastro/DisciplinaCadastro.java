    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed
        String msg = "Ops, algo de errado não está certo..";
        try{
            int id = Integer.parseInt(disciplinaIDInput.getText());
            int hora = Integer.parseInt(disciplinaHoraInput.getText());
            String nome = disciplinaNomeInput.getText();
            
            disciplina = new Disciplina(id, hora, nome);
            DisciplinaDAO disciplinas = new DisciplinaDAO();
        
            if(editar && disciplinas.atualizar(disciplina)){
                msg = "Cadastro Atualizado!";
            } else if(disciplinas.adicionar(disciplina)) {
                msg = "Cadastro Efetuado!";
                disciplinaIDInput.setText("");
                disciplinaNomeInput.setText("");
                disciplinaHoraInput.setText("");
            }
        } catch(Exception e){
            e.printStackTrace();
            
        } finally {
            JOptionPane.showMessageDialog(null,msg);
        }
        
    }//GEN-LAST:event_cadastrarActionPerformed



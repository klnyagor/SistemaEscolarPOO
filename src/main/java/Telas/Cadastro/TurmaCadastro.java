    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed
        String msg = "Ops, algo de errado n√£o esta certo..";
        try{
            int id = Integer.parseInt(turmaIDInput.getText());
            Curso curso = (Curso) TurmaBoxCurso.getSelectedItem();
            Professor prof = (Professor) TurmaBoxProfessor.getSelectedItem();
            Disciplina disc = (Disciplina) TurmaBoxDisciplina.getSelectedItem();
            int horario = Integer.parseInt(turmaHorarioInput.getText());
            String turno = (String) TurmaBoxTurno.getSelectedItem();
            String periodo = turmaPeriodoInput.getText();
            turma = new Turma(id, curso, prof, disc, horario, turno, periodo);
            TurmaDAO turmas = new TurmaDAO();
            
            if(editar && turmas.atualizar(turma)){
                msg = "Turma Atualizada!";
            } else if(turmas.adicionar(turma)){
                msg = "Turma Adicionada!";
                //limpar formulario
                turmaIDInput.setText("");
                turmaHorarioInput.setText("");
                turmaPeriodoInput.setText("");
            }
        } catch(Exception e){
            e.printStackTrace();
            
        } finally {
            JOptionPane.showMessageDialog(null, msg);
        }
        
    }//GEN-LAST:event_cadastrarActionPerformed


    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed
        String msg = "Ops, algo de errado não esta certo..";
        try{
            int id = Integer.parseInt(professorIdInput.getText());
            String nome = professorNomeInput.getText();
            String endereco = professorEnderecoInput.getText();
            String email = professorEmailInput.getText();
            double salario = Double.parseDouble(professorSalarioInput.getText());
            professor = new Professor(salario, nome, endereco, nome, email, id);
            ProfessorDAO professores = new ProfessorDAO();
        
            if(editar && professores.atualizar(professor)){
                msg = "Cadastro Atualizado!";
            } else if (professores.adicionar(professor)){
                msg = "Cadastro Efetuado!";
                //limpar formulario
                professorIdInput.setText("");
                professorNomeInput.setText("");
                professorEnderecoInput.setText("");
                professorTelefoneInput.setText("");
                professorEmailInput.setText("");
                professorSalarioInput.setText("");   
            }
        } catch(Exception e){
            e.printStackTrace();
        } finally {
            JOptionPane.showMessageDialog(null, msg);
        }
    }//GEN-LAST:event_cadastrarActionPerformed


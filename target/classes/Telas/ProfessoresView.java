    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linha = -1;
        linha = TabelaProfessores.getSelectedRow();
        if(linha >= 0){
            int id = (int) TabelaProfessores.getValueAt(linha, 0);
            ProfessorDAO professores = new ProfessorDAO();
            
            if(professores.deletar(id)){
                JOptionPane.showMessageDialog(null, "Professor Removido!");
                btnPesquisarActionPerformed(null);
            } else {
                JOptionPane.showMessageDialog(null, "Professor não pode ser Removido!");
            }
            
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed


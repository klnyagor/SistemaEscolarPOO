    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        DefaultTableModel tabela = (DefaultTableModel) TabelaProfessores.getModel();
        tabela.setNumRows(0);
        ProfessorDAO professor = new ProfessorDAO();
        for(Professor p: professor.listar()){
            tabela.addRow(new Object[]{ p.getId(), p.getNome(), p.getEndereco(), p.getTelefone(), p.getEmail(), p.getSalario() });
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int linha = -1;
        linha = TabelaProfessores.getSelectedRow();
        if(linha >= 0){
            int id = (int) TabelaProfessores.getValueAt(linha, 0);
            String nome = (String) TabelaProfessores.getValueAt(linha, 1);
            String endereco = (String) TabelaProfessores.getValueAt(linha, 2);
            String telefone = (String) TabelaProfessores.getValueAt(linha, 3);
            String email = (String) TabelaProfessores.getValueAt(linha, 4);
            double salario = (double) TabelaProfessores.getValueAt(linha, 5);
            
            ProfessorCadastro cadastro = new ProfessorCadastro(menu, new Professor(salario, nome, endereco, telefone, email, id));
            controller.navegar(cadastro);
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha!");
        }
        
    }//GEN-LAST:event_btnEditarActionPerformed


